    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String id = txtId.getText().trim();
        if(id.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please Select Ticket Number First", "Incomplete", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try{
            boolean result = TicketDAO.calcelTicket(id);
            if(result){
                JOptionPane.showMessageDialog(null, "Record Deleted Sucessfully","Sucess",JOptionPane.INFORMATION_MESSAGE);
                clearText();
                loadTicketNo();
                tb.setRowCount(0);
                loadTickets();
                
                return;
            }else
                JOptionPane.showMessageDialog(null, "Sorry Record is not deleted","Incomplete",JOptionPane.ERROR_MESSAGE);

        }catch(SQLException se){
            JOptionPane.showMessageDialog(null, "Error in Database","Incomplete",JOptionPane.ERROR_MESSAGE);
            se.printStackTrace();
            return;
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(!isEmpty()){
            JOptionPane.showMessageDialog(null, "Please Fill all the value properly","Incomplete",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            TicketPojo ticket = new TicketPojo(); 
            ticket.setTicket_no(txtId.getText());
            ticket.setPid(jcName.getSelectedItem().toString());
            ticket.setDay(new java.sql.Date(jcDate.getDate().getTime()));
            ticket.setTime(jcTime.getSelectedItem().toString());
            ticket.setBusno(jcBusNo.getSelectedItem().toString());
            ticket.setSource(jcSource.getSelectedItem().toString());
            ticket.setDestination(jcDestination.getSelectedItem().toString());
            int price = Integer.parseInt(txtPrice.getText());
            ticket.setPrice(price);
            boolean result = TicketDAO.bookTicket(ticket);
            if(result == true){
                JOptionPane.showMessageDialog(null, "Record Added Sucessfully","Sucess",JOptionPane.INFORMATION_MESSAGE);
                clearText();
                loadTicketNo();
                tb.setRowCount(0);
                loadTickets();
                
                return;
            }else
            JOptionPane.showMessageDialog(null, "Sorry Record is not Added","Incomplete",JOptionPane.ERROR_MESSAGE);

        }catch(SQLException se){
            JOptionPane.showMessageDialog(null, "Error in Database","Incomplete",JOptionPane.ERROR_MESSAGE);
            se.printStackTrace();
            return;
        }
        catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "Please Enter numeric value in price","Incomplete",JOptionPane.ERROR_MESSAGE);
            nfe.printStackTrace();
            return;
        }
    }//GEN-LAST:event_btnAddActionPerformed

