    private void loadTickets() {
        try{
            List<TicketPojo> ticketLists = TicketDAO.getAllTickets();
            Map<String,String> passengerLists = PassengerDAO.getPassengerName();
            tb = (DefaultTableModel)tbTickets.getModel();
            Object []ticket = new Object[9];
            for(TicketPojo ticketList: ticketLists){
                ticket[0] = ticketList.getTicket_no();
                ticket[1] = ticketList.getPid();
                if(passengerLists.containsKey(ticketList.getPid())){
                    ticket[2] = passengerLists.get(ticketList.getPid());
                }
                ticket[3] = ticketList.getDay();
                ticket[4] = ticketList.getTime();
                ticket[5] = ticketList.getBusno();
                ticket[6] = ticketList.getSource();
                ticket[7] = ticketList.getDestination();
                ticket[8] = ticketList.getPrice();
                tb.addRow(ticket);
            }
        }catch(SQLException se){
            JOptionPane.showMessageDialog(null, "Unable to connect with database", "Error", JOptionPane.ERROR_MESSAGE);
            se.printStackTrace();
        }  
    }

    private void loadOtherDetails() {
        try{
            List<BusPojo> allBusDetails = BusDAO.getBusDetails();
            List<PassengerPojo> allPassengers = PassengerDAO.getAllPassenger();
            for(BusPojo allBus : allBusDetails){
                String id = allBus.getBusNo();
                jcBusNo.addItem(id);
            }
            for(PassengerPojo allPassenger : allPassengers){
                String pid = allPassenger.getPid();
                jcName.addItem(pid);
            }
        }
        catch(SQLException se){
            JOptionPane.showMessageDialog(null, "Unable to get bus id.","Error",JOptionPane.ERROR);
            se.printStackTrace();
            return;
        }
    
    }

